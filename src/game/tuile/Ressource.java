package game.tuile;

/**
 * Enum representing the different types of resources in the game
 * Each constant in this enum represents a specific resource that can be used or collected during the game
 */
public enum Ressource {
    
    /** Represents Wood resource */
    WOOD,
    
    /** Represents Wealth resource */
    WEALTH,
    
    /** Represents Ore resource */
    ORE,
    
    /** Represents Sheep resource */
    SHEEP;
}
